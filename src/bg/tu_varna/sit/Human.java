package bg.tu_varna.sit;

public class Human {
    String firstName;
    String secondName;
    String thirdName;
    char gender;
    int age;
    public Human(String firstName, String secondName, String thirdName, char gender, int age) {
        this.firstName = firstName;
        this.secondName = secondName;
        this.thirdName = thirdName;
        this.gender = gender;
        this.age = age;
    }

    
}
